// API para download de m√≠dia
app.get('/api/media/download/:fileName', async (req, res) => {
  let pool, server;
  try {
    const { fileName } = req.params;
    console.log('üîç Buscando arquivo para download:', fileName);
    
    ({ pool, server } = await getDbPoolWithTunnel());
    
    // Primeiro, tentar buscar na tabela cancelamento_pdfs (anexos)
    let [rows] = await pool.query(`
      SELECT * FROM cancelamento_pdfs 
      WHERE file_name = ?
    `, [fileName]);
    
    // Se n√£o encontrar na tabela cancelamento_pdfs, buscar na tabela media
    if (rows.length === 0) {
      console.log('üìã Arquivo n√£o encontrado em cancelamento_pdfs, buscando em media...');
      [rows] = await pool.query(`
        SELECT * FROM media 
        WHERE file_name = ?
      `, [fileName]);
    }
    
    if (rows.length === 0) {
      console.log('‚ùå Arquivo n√£o encontrado no banco:', fileName);
      return res.status(404).json({ error: 'Arquivo n√£o encontrado' });
    }
    
    const fileData = rows[0];
    console.log('‚úÖ Arquivo encontrado no banco');
    
    // Verificar se existe alguma coluna com dados bin√°rios
    const binaryColumns = ['file_content', 'file_data', 'data', 'content', 'binary_data'];
    let binaryData = null;
    let binaryColumn = null;
    
    for (const col of binaryColumns) {
      if (fileData[col]) {
        binaryData = fileData[col];
        binaryColumn = col;
        break;
      }
    }
    
    if (binaryData) {
      console.log(`‚úÖ Dados bin√°rios encontrados na coluna: ${binaryColumn}`);
      
      const mimeType = fileData.mime_type || 'application/octet-stream';
      
      // Configurar headers para download
      res.setHeader('Content-Type', mimeType);
      res.setHeader('Content-Disposition', `attachment; filename="${fileName}"`);
      
      // Enviar dados bin√°rios diretamente do banco
      res.send(binaryData);
      
      console.log('‚úÖ Download conclu√≠do para:', fileName);
    } else {
      console.log('‚ùå Nenhuma coluna com dados bin√°rios encontrada');
      console.log('üìã Colunas dispon√≠veis:', Object.keys(fileData));
      
      // Fallback: tentar usar file_path se n√£o houver dados bin√°rios
      const filePath = fileData.file_path;
      const mimeType = fileData.mime_type || 'application/octet-stream';
      
      if (fs.existsSync(filePath)) {
        console.log('üìÅ Usando fallback: arquivo do sistema');
        res.setHeader('Content-Type', mimeType);
        res.download(filePath);
      } else {
        return res.status(404).json({ 
          error: 'Dados do arquivo n√£o encontrados no banco',
          availableColumns: Object.keys(fileData),
          message: 'O arquivo precisa estar armazenado como dados bin√°rios no banco'
        });
      }
    }
    
  } catch (error) {
    console.error('‚ùå Erro ao fazer download:', error);
    res.status(500).json({ error: 'Erro ao fazer download', details: error.message });
  }
  // N√£o fechar conex√£o - ser√° reutilizada
});

