app.put('/api/usuarios/:id', async (req, res) => {
  let pool;
  try {
    const { id } = req.params;
    const { role, status, nome, email } = req.body || {};
    ({ pool } = await getDbPoolWithTunnel());

    // Montar atualiza√ß√£o parcial
    const fields = [];
    const values = [];
    if (role) { fields.push('role = ?'); values.push(role); }
    if (status) { fields.push('status = ?'); values.push(status); }
    if (nome) { fields.push('nome = ?'); values.push(nome); }
    if (email) { fields.push('email = ?'); values.push(email); }

    if (fields.length === 0) {
      return res.status(400).json({ error: 'Nenhum campo para atualizar' });
    }

    values.push(id);
    await pool.query(`UPDATE usuarios SET ${fields.join(', ')} WHERE id = ?`, values);
    res.json({ success: true });
  } catch (err) {
    console.error('‚ùå Erro ao atualizar usu√°rio:', err);
    res.status(500).json({ error: 'Erro ao atualizar usu√°rio', details: err.message });
  }
});

