app.get('/api/audios/stream/:fileName', async (req, res) => {
  let pool, server;
  try {
    const { fileName } = req.params;
    console.log('üîç Buscando √°udio para streaming:', fileName);
    
    ({ pool, server } = await getDbPoolWithTunnel());
    
    // Buscar o √°udio com todas as colunas
    const [mediaRows] = await pool.query(`
      SELECT * FROM media 
      WHERE file_name = ? AND media_type = 'audio'
    `, [fileName]);
    
    if (mediaRows.length === 0) {
      console.log('‚ùå √Åudio n√£o encontrado no banco:', fileName);
      return res.status(404).json({ error: '√Åudio n√£o encontrado' });
    }
    
    const audioData = mediaRows[0];
    console.log('‚úÖ √Åudio encontrado no banco');
    
    // Verificar se existe alguma coluna com dados bin√°rios
    const binaryColumns = ['file_content', 'file_data', 'data', 'content', 'binary_data'];
    let binaryData = null;
    let binaryColumn = null;
    
    for (const col of binaryColumns) {
      if (audioData[col]) {
        binaryData = audioData[col];
        binaryColumn = col;
        break;
      }
    }
    
    if (binaryData) {
      console.log(`‚úÖ Dados bin√°rios encontrados na coluna: ${binaryColumn}`);
      
      const mimeType = audioData.mime_type || 'audio/mpeg';
      const fileSize = audioData.file_size_bytes || binaryData.length;
      const range = req.headers.range;

      // Configurar headers CORS para streaming
      res.setHeader('Access-Control-Allow-Origin', '*');
      res.setHeader('Access-Control-Allow-Methods', 'GET, HEAD');
      res.setHeader('Access-Control-Allow-Headers', 'Range');
      res.setHeader('Accept-Ranges', 'bytes');

      if (range && fileSize > 0) {
        // Suporte a Range Requests (streaming)
        const parts = range.replace(/bytes=/, "").split("-");
        const start = parseInt(parts[0], 10);
        const end = parts[1] ? parseInt(parts[1], 10) : fileSize-1;
        const chunksize = (end-start)+1;
        
        console.log(`üìä Streaming range: ${start}-${end}/${fileSize} (${chunksize} bytes)`);
        
        const head = {
          'Content-Range': `bytes ${start}-${end}/${fileSize}`,
          'Accept-Ranges': 'bytes',
          'Content-Length': chunksize,
          'Content-Type': mimeType,
        };
        res.writeHead(206, head);
        
        // Enviar apenas a parte solicitada dos dados
        const buffer = Buffer.from(binaryData);
        res.end(buffer.slice(start, end + 1));
      } else {
        // Streaming completo
        console.log(`üìä Streaming completo: ${fileSize} bytes`);
        
        res.setHeader('Content-Type', mimeType);
        res.setHeader('Content-Length', fileSize);
        
        // Enviar todos os dados do √°udio
        res.send(binaryData);
      }
      
      console.log('‚úÖ Streaming conclu√≠do para:', fileName);
    } else {
      console.log('‚ùå Nenhuma coluna com dados bin√°rios encontrada');
      console.log('üìã Colunas dispon√≠veis:', Object.keys(audioData));
      return res.status(404).json({ 
        error: 'Dados do √°udio n√£o encontrados no banco',
        availableColumns: Object.keys(audioData),
        message: 'O √°udio precisa estar armazenado como dados bin√°rios no banco'
      });
    }
    
  } catch (error) {
    console.error('‚ùå Erro ao fazer streaming:', error);
    res.status(500).json({ error: 'Erro ao fazer streaming do √°udio', details: error.message });
  }
  // N√£o fechar conex√£o - ser√° reutilizada
});

